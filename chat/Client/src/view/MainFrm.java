    private void init() {
        mySocket = new ClientCtr(this);
        if (!mySocket.openConnection()) {
            resetClient();
        } else {
            mySocket.getActiveFunction().add(new ObjectWrapper(ObjectWrapper.REPLY_LOGIN_USER, this));
        }
    }

    public void resetClient() {
        if (mySocket != null) {
            mySocket.closeConnection();
            mySocket.getActiveFunction().clear();
            mySocket = null;
        }
    }

    public void receivedDataProcessing(ObjectWrapper data) {
        //        reply login
        if (data.getPerformative() == 2) {
            if (data.getData() != null) {
                sidebar.setVisible(true);
                loginPane.setVisible(false);
                this.info = (User) data.getData();
            } else {
                JOptionPane.showMessageDialog(this, "Login fail!");
            }
        }
    }

